import UIKit

extension UIColor {
    
    class func primaryColor() -> UIColor {
        return UIColor(red: 255/255, green: 55/255, blue: 0/255, alpha: 1)
    }
    class func primaryLightColor() -> UIColor {
        return UIColor(red: 255/255, green: 212/255, blue: 200/255, alpha: 1)
    }
    
    class func lpBlackColor() -> UIColor {
        return UIColor(red: 41/255, green: 39/255, blue: 36/255, alpha: 1)
    }
}
